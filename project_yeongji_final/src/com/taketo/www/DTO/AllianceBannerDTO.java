package com.taketo.www.DTO;

public class AllianceBannerDTO {
	private int no;
	private String allianceName;
	private String allianceBanner;
	private String allianceInfo;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getAllianceName() {
		return allianceName;
	}
	public void setAllianceName(String allianceName) {
		this.allianceName = allianceName;
	}
	public String getAllianceBanner() {
		return allianceBanner;
	}
	public void setAllianceBanner(String allianceBanner) {
		this.allianceBanner = allianceBanner;
	}
	public String getAllianceInfo() {
		return allianceInfo;
	}
	public void setAllianceInfo(String allianceInfo) {
		this.allianceInfo = allianceInfo;
	}
	
}
